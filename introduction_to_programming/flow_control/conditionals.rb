# A conditional is a fork or many forks in the road.  Your data approaches a conditional and the conditional then tells the data wehre to go basedo n some defined parameters.  

# conditional.rb 

puts "Put in a number" 
a = gets.chomp.to_i 

if a == 3
  puts "a is 3"
elsif a == 4 
  puts "a is 4" 
else 
  puts "a is neither 3, nor 4"
end 